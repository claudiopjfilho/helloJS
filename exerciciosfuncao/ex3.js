// 03) Crie uma função que recebe dois parâmetros, base e expoente, e retorne a base elevada ao expoente
function fatoracao(base, expoente) {
    return base ** expoente //modo novo
    //(Math.pow(base ,expoente))modo antigo
   
}
console.log(fatoracao(3, 2))
console.log(fatoracao(2, 2))
console.log(fatoracao(5, 5))

